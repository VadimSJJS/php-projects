function getCountries($films) {
    $countries = [];
    foreach ($films as $film) {
        if (!in_array($film['country'], $countries)) {
            $countries[] = $film['country'];
        }
    }
    sort($countries);
    return $countries;
}

function getYears($films) {
    $years = [];
    foreach ($films as $film) {
        if (!in_array($film['year'], $years)) {
            $years[] = $film['year'];
        }
    }
    rsort($years);
    return $years;
}

